// Description: A simple calculator program that takes 2 numbers to either add, subtract or divide



package com.keyin.qap1;

public class SimpleCalculator {

    private int num1;
    private int num2;


    public int add (int num1, int num2){
        return num1 + num2;
    }

    public int sub (int num1, int num2){
        return num1 - num2;
    }

    public int div (int num1, int num2){
        return num1 / num2;
    }
}
